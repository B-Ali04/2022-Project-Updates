select
    SPRIDEN.SPRIDEN_ID BANNER_ID,
    SPRIDEN_LAST_NAME Last_Name,
    SPRIDEN_FIRST_NAME First_Name,
    STVCLAS.STVCLAS_CODE Student_Class, 
    GORADID.GORADID_ADDITIONAL_ID SUID,
    SGBSTDN.SGBSTDN_STYP_CODE Reg_Type,
    STVMAJR.STVMAJR_DESC Program_of_Study,
    STVDEGC.STVDEGC_CODE Degree_Prog,
    r.LFMI_NAME Instructor_Name,
    SSBSECT.SSBSECT_SICAS_CAMP_COURSE_ID Course_Key,
    SSBSECT.SSBSECT_CRN Course_CRN,
    SCBCRSE.SCBCRSE_TITLE Course_Title,    
    SSBSECT.SSBSECT_SEQ_NUMB Seq_Numb,
    SFRSTCR.SFRSTCR_GRDE_CODE Letter_Grade,
    STVTERM1.STVTERM_DESC cOURSE_Term_Code,
    SFRSTCR.SFRSTCR_GRDE_DATE Activity_Date,
    STVCLAS.STVCLAS_SURROGATE_ID

from
    SPRIDEN SPRIDEN

    join STVTERM STVTERM on STVTERM.STVTERM_CODE = 202320

    join GORADID GORADID on GORADID.GORADID_PIDM = SPRIDEN.SPRIDEN_PIDM
        and GORADID.GORADID_ADID_CODE = 'SUID'

    join SFRSTCR SFRSTCR on SFRSTCR.SFRSTCR_PIDM = SPRIDEN.SPRIDEN_PIDM
        and SFRSTCR.SFRSTCR_RSTS_CODE in ('RW', 'RE')

    left outer join SSBSECT SSBSECT on SSBSECT.SSBSECT_CRN = SFRSTCR.SFRSTCR_CRN
        and SSBSECT.SSBSECT_TERM_CODE = SFRSTCR.SFRSTCR_TERM_CODE

    left outer join rel_course_instructors r on r.crn = SSBSECT.SSBSECT_CRN
        and r.term_Code = SSBSECT.SSBSECT_TERM_CODE

    left outer join SCBCRSE SCBCRSE on SSBSECT.SSBSECT_SUBJ_CODE = SCBCRSE.SCBCRSE_SUBJ_CODE
        and SCBCRSE.SCBCRSE_CRSE_NUMB = SSBSECT.SSBSECT_CRSE_NUMB
        and SCBCRSE.SCBCRSE_EFF_TERM = (select max(SCBCRSE_EFF_TERM)
                          from   SCBCRSE SCBCRSE1
                          where  SCBCRSE1.SCBCRSE_SUBJ_CODE = SSBSECT_SUBJ_CODE
                          and    SCBCRSE1.SCBCRSE_CRSE_NUMB = SSBSECT_CRSE_NUMB
                          and    SCBCRSE1.SCBCRSE_EFF_TERM <= STVTERM.STVTERM_CODE)
                          
    join SGBSTDN SGBSTDN on SGBSTDN.SGBSTDN_PIDM = SPRIDEN.SPRIDEN_PIDM
        and SGBSTDN.SGBSTDN_TERM_CODE_EFF = fy_sgbstdn_eff_term(SGBSTDN.SGBSTDN_PIDM, SFRSTCR.SFRSTCR_TERM_CODE)
        and SGBSTDN.SGBSTDN_MAJR_CODE_1 not in ('0000', 'EHS', 'SUS', 'VIS')
        and SGBSTDN.SGBSTDN_STST_CODE in ('IG', 'IR','AS')
                          
    left outer join STVTERM STVTERM1 on STVTERM1.STVTERM_CODE = SFRSTCR.SFRSTCR_TERM_CODE

    left outer join STVMAJR STVMAJR on STVMAJR.STVMAJR_CODE = SGBSTDN.SGBSTDN_MAJR_CODE_1
    
    left outer join STVDEGC STVDEGC on STVDEGC.STVDEGC_CODE = SGBSTDN.SGBSTDN_DEGC_CODE_1
    
    left outer join STVCLAS STVCLAS on STVCLAS.STVCLAS_CODE = f_class_calc_fnc(SGBSTDN.SGBSTDN_PIDM,SGBSTDN.SGBSTDN_LEVL_CODE, STVTERM.STVTERM_CODE)
                                       
where
    SPRIDEN.SPRIDEN_NTYP_CODE is null
    and SPRIDEN.SPRIDEN_CHANGE_IND is null
    
    --and SFRSTCR.SFRSTCR_TERM_CODE > STVTERM.STVTERM_CODE --NEXT TERM REGISTRATION
    --and SFRSTCR.SFRSTCR_TERM_CODE = STVTERM.STVTERM_CODE -- CURRENT COURSE REGISTRATION
    --and SFRSTCR.SFRSTCR_TERM_CODE <= STVTERM.STVTERM_CODE -- COURSE REG HISTORY
    --and SFRSTCR.SFRSTCR_GRDE_CODE is not null -- COURSE GRADE HISTORY(ADD TO ABOVE)
    --and SFRSTCR.SFRSTCR_TERM_CODE >= STVTERM.STVTERM_CODE -- CURRENT AND FUTURE COURSE REGISTRATIONS
    --LEAVE BLANK FOR ALL REGISTERED COURSES

--$addfilter

--$beginorder

order by
      SPRIDEN.SPRIDEN_LAST_NAME, SFRSTCR.SFRSTCR_TERM_CODE asc, SSBSECT.SSBSECT_SICAS_CAMP_COURSE_ID
    --SFRSTCR.SFRSTCR_GRDE_DATE--, ssbsect_SURROGATE_ID, SPRIDEN.SPRIDEN_LAST_NAME
    --QUESTIONS: WHICH PROFESSORS ARE GETTING TO THEIR STUDENTS SOONER. AND WHICH COURSES ARE DOING THE SAME REGARDLESS OF INSTRUCTOR. R2 CLASS COUNT, LOCATION, TERM, AND IS IT COVIDED.
--$endorder

